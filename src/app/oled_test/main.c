int main(void)
{
    rcc_set_sysclk(RCC_HSI, 0);
    rcc_enable_gpio('B'); rcc_enable_gpio('C'); rcc_enable_i2c(1);

    /* LED */
    GpioPin_ctor(&_led, GPIOC, GPIO_PIN_13);
    gpio_set_mode(&_led, GPIO_MODE_OUT_PP); L_OFF();

    /* I2C GPIO */
    GpioPin scl, sda;
    GpioPin_ctor(&scl, GPIOB, GPIO_PIN_6);
    gpio_set_mode(&scl, GPIO_CNF_ALT_OD | GPIO_MODE_OUT_50MHZ);
    GpioPin_ctor(&sda, GPIOB, GPIO_PIN_7);
    gpio_set_mode(&sda, GPIO_CNF_ALT_OD | GPIO_MODE_OUT_50MHZ);
    gpio_set(&scl,1); gpio_set(&sda,1);

    /* I2C init via our HAL */
    I2cPort i2c_p;
    I2cPort_ctor(&i2c_p, I2C1, 400000, 8000000);  /* 400kHz fast mode */
    i2c_init(&i2c_p);
    g_i2c = I2C1;

    /* ── Init (exact same sequence as DShanMCU-F103 reference) ── */
    oled_cmd(0xAE);        /* Display OFF */
    oled_cmd(0x20); oled_cmd(0x02);  /* PAGE_ADDR_MODE */
    oled_cmd(0xA8); oled_cmd(0x3F);  /* MUX=64 */
    oled_cmd(0xD3); oled_cmd(0x00);  /* Offset=0 */
    oled_cmd(0x40);                   /* Start line=0 */
    oled_cmd(0xA1);                   /* Segment remap */
    oled_cmd(0xC8);                   /* COM remap */
    oled_cmd(0xDA); oled_cmd(0x12);  /* COM pins */
    oled_cmd(0x81); oled_cmd(0xFF);  /* Contrast=max */
    oled_cmd(0xA4);                   /* Normal */
    oled_cmd(0xA6);                   /* Not inverted */
    oled_cmd(0xD5); oled_cmd(0x80);  /* Clock */
    oled_cmd(0x8D); oled_cmd(0x14);  /* Charge pump ON */
    oled_cmd(0xAF);                   /* DISPLAY ON */

    /* ── Reference font (8x16, from DShanMCU-F103 ascii_font.c) ── */
    extern const uint8_t ascii_font[][16];

    /* Clear all pages */
    for (int p = 0; p < 8; p++) {
        oled_cmd(0xB0 + p); oled_cmd(0x00); oled_cmd(0x10);
        uint8_t z[129]; z[0] = 0x40; for (int i = 1; i < 129; i++) z[i] = 0;
        i2c_send(g_i2c, OLED_ADDR, z, 129);
    }

    /* Display "HELLO" using reference's method:
       each char = 8 cols wide, 2 pages tall. x in chars, y in pages. */
    const char *msg = "HELLO!";
    uint8_t x_char = 0;  /* character column, 0-15 */

    for (int ci = 0; msg[ci]; ci++) {
        unsigned char c = (unsigned char)msg[ci];
        if (c < 32 || c > 127) c = 32;
        uint8_t col = x_char * 8;  /* pixel column */

        /* Upper half (page 0) */
        oled_cmd(0xB0 + 0);             /* page = 0 */
        oled_cmd(0x00 + (col & 0x0F));
        oled_cmd(0x10 + (col >> 4));
        { uint8_t b[9]; b[0] = 0x40; for (int i=0;i<8;i++) b[1+i]=ascii_font[c][i];   i2c_send(g_i2c,OLED_ADDR,b,9); }

        /* Lower half (page 1) */
        oled_cmd(0xB0 + 1);             /* page = 1 */
        oled_cmd(0x00 + (col & 0x0F));
        oled_cmd(0x10 + (col >> 4));
        { uint8_t b[9]; b[0] = 0x40; for (int i=0;i<8;i++) b[1+i]=ascii_font[c][i+8]; i2c_send(g_i2c,OLED_ADDR,b,9); }

        x_char++;
    }

    /* Heartbeat */
    while (1) { L_ON(); dly(400000); L_OFF(); dly(8000000); }
}
