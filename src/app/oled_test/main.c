/* ── LED ────────────────────────────────────────────────── */
static GpioPin _led;
static void L_ON(void)  { gpio_set(&_led, 0); }
static void L_OFF(void) { gpio_set(&_led, 1); }
static void dly(uint32_t n) { while (n--) __asm__("nop"); }

/* ── I2C 底层操作 ─────────────────────────────────────── */
#define I2C_TO 100000

static bool i2c_send(I2C_Type *i, uint8_t addr, const uint8_t *buf, size_t len)
{
    uint32_t to;

    /* Wait idle */
    to = I2C_TO; while ((i->SR2 & I2C_SR2_BUSY) && --to) {}

    /* START */
    i->CR1 |= I2C_CR1_START;
    to = I2C_TO; while (!(i->SR1 & I2C_SR1_SB) && --to) {}
    if (!to) { i->CR1 |= I2C_CR1_STOP; return false; }

    /* ADDR */
    i->DR = (uint8_t)(addr << 1);
    to = I2C_TO; while (!(i->SR1 & (I2C_SR1_ADDR | I2C_SR1_AF)) && --to) {}
    if (!to || (i->SR1 & I2C_SR1_AF)) { (void)i->SR2; i->CR1 |= I2C_CR1_STOP; return false; }
    (void)i->SR2;

    /* DATA */
    for (size_t n = 0; n < len; n++) {
        to = I2C_TO; while (!(i->SR1 & I2C_SR1_TXE) && --to) {}
        if (!to) { i->CR1 |= I2C_CR1_STOP; return false; }
        i->DR = buf[n];
    }

    /* BTF then STOP */
    to = I2C_TO; while (!(i->SR1 & I2C_SR1_BTF) && --to) {}
    i->CR1 |= I2C_CR1_STOP;
    return true;
}

/* ── OLED 命令/数据 ────────────────────────────────────── */
#define OLED_ADDR  0x3C   /* 7-bit (our i2c_send left-shifts to 0x78) */

