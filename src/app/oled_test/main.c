static bool  oled_cmd(uint8_t c) { uint8_t b[2]={0x00,c}; return i2c_send(g_i2c, OLED_ADDR, b, 2); }
static bool  oled_dat(uint8_t d) { uint8_t b[2]={0x40,d}; return i2c_send(g_i2c, OLED_ADDR, b, 2); }

